
public class LoopDemo {

	public static void main(String[] args) {
		int a=1;
		while(a<=5) {
			System.out.println(a);
			a++;
		}
	System.out.println("--------------------");	 
		int b=1;
		do {
			System.out.println(b);
			 b++;
		}while(b<=5);
	System.out.println("--------------------");	 	
	  for(int c=1;c<=5;c++) {
		  System.out.println(c);
	  }	
	}

}
